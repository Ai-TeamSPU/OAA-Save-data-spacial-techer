function getCurriculaByLevel(level) {
  if (level === "ปริญญาตรี") return ALL_CURRICULA.filter(c => c.includes("บัณฑิต") && !c.includes("มหาบัณฑิต") && !c.includes("ดุษฎีบัณฑิต"));
  if (level === "ปริญญาโท") return ALL_CURRICULA.filter(c => c.includes("มหาบัณฑิต") || c.includes("Master"));
  if (level === "ปริญญาเอก") return ALL_CURRICULA.filter(c => c.includes("ดุษฎีบัณฑิต") || c.includes("Doctor") || c.includes("PhD"));
  return ALL_CURRICULA;
}

