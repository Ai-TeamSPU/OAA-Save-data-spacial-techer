// ===== Helper: สัดส่วนจากรหัสวิชา =====
function getProportionFromCode(subjectEntry) {
  if (!subjectEntry) return "";
  const code = subjectEntry.split(" - ")[0].trim();
  if (/^[A-Z]{3}\d{3}$/.test(code)) return "50%";
  if (/^[A-Z]{3}\d{5}$/.test(code)) return "ไม่มีกำหนด";
  return "";
}

// ===== Helper: นับระยะเวลา =====
function calcDuration(startDate, endDate) {
  if (!startDate) return "";
  const start = new Date(startDate);
  const end = endDate ? new Date(endDate) : new Date();
  let months = (end.getFullYear() - start.getFullYear()) * 12 + (end.getMonth() - start.getMonth());
  if (months < 0) return "";
  const years = Math.floor(months / 12);
  const rem = months % 12;
  if (years === 0 && rem === 0) return "น้อยกว่า 1 เดือน";
  if (years === 0) return `${rem} เดือน`;
  if (rem === 0) return `${years} ปี`;
  return `${years} ปี ${rem} เดือน`;
}


